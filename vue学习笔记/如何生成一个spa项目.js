// 前提准备
`
安装 cnpm(npm的镜像)
npm install -g cnpm --registry=https://registry.npm.taobao.org

非项目应用使用vue
cnpm i vue --save
然后在html脚本中导入这个vue.js文件即可
`

`
使用vue-cli构建spa项目
进入项目文件夹
cnpm install -g vue-cli
初始化一个项目
vue init webpack-simple demo1
or
vue init webpack demo1
启动该程序
cnpm install(下载程序启动插件)
cnpm run dev(启动服务器)
cnpm run build(build for productin with minification)
`

